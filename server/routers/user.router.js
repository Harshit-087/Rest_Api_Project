import express from "express"
import {registerUser,signinUser,SignoutUser} from "../controller/user.controllers.js"

const userRouter = express.Router();
/**
 * @swagger
 * /user-api/v1/signup:
 *   post:
 *     summary: Register new user
 *     tags: [User]
 *     requestBody:
 *       required: true
 *       content:
 *         application/json:
 *           schema:
 *             type: object
 *             required:
 *               - name
 *               - email
 *               - password
 *             properties:
 *               name:
 *                 type: string
 *                 example: John Doe
 *               email:
 *                 type: string
 *                 example: john@example.com
 *               password:
 *                 type: string
 *                 example: 123456
 *     responses:
 *       201:
 *         description: User registered successfully
 *         content:
 *           application/json:
 *             schema:
 *               type: object
 *               properties:
 *                 success:
 *                   type: boolean
 *                   example: true
 *                 message:
 *                   type: string
 *                   example: User registered successfully
 *
 * /user-api/v1/signin:
 *   post:
 *     summary: Signin user
 *     tags: [User]
 *     requestBody:
 *       required: true
 *       content:
 *         application/json:
 *           schema:
 *             type: object
 *             required:
 *               - email
 *               - password
 *             properties:
 *               email:
 *                 type: string
 *                 example: john@example.com
 *               password:
 *                 type: string
 *                 example: 123456
 *     responses:
 *       200:
 *         description: User logged in successfully
 *         content:
 *           application/json:
 *             schema:
 *               type: object
 *               properties:
 *                 token:
 *                   type: string
 *                   example: jwt_token_here
 *                 message:
 *                   type: string
 *                   example: Login successful
 *
 * /user-api/v1/signout:
 *   post:
 *     summary: Signout user
 *     tags: [User]
 *     responses:
 *       200:
 *         description: Signout successfully
 *         content:
 *           application/json:
 *             schema:
 *               type: object
 *               properties:
 *                 message:
 *                   type: string
 *                   example: Logout successful
 */
userRouter.post("/signup",registerUser)


userRouter.post("/signin",signinUser)


userRouter.post("/signout",SignoutUser)

export default userRouter;